const parseSystemTemplates = (): Template[] => {
    const templates: Template[] = [];
    if (!rawTemplatesText) return templates;

    const templateBlocks = rawTemplatesText.split('---TEMPLATE_START---').slice(1);
    
    for (const block of templateBlocks) {
        const contentParts = block.split('---CONTENT_START---');
        if (contentParts.length !== 2) continue;
        
        const [header, contentBody] = contentParts;
        const content = contentBody.split('---TEMPLATE_END---')[0]?.trim() ?? '';
        
        let title = (header.match(/title:(.*)/)?.[1] || '').trim();
        const studyType = (header.match(/studyType:(.*)/)?.[1] || 'General').trim();
        const scenario = (header.match(/scenario:(.*)/)?.[1] || 'General').trim();

        if (!title) {
            if (content.startsWith("Wypełnij pusta tabele")) {
                title = "Wypełnij tabelę pacjentów";
            } else {
                continue; // Skip templates with no title
            }
        }

        templates.push({
            id: `system-${crypto.randomUUID()}`,
            title,
            studyType,
            scenario,
            content,
            isSystem: true,
        });
    }
    return templates;
};

export const systemTemplates: Template[] = parseSystemTemplates();